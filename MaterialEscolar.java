package trab1;

public class MaterialEscolar extends RecursosMateriais{
    public int lapis = 0;
    public int caderno = 0;
    public int caneta = 0;

    public int getLapis() {
        return this.lapis;
    }

    public void setLapis(int lapis) {
        this.lapis = lapis;
    }

    public int getCaderno() {
        return this.caderno;
    }

    public void setCaderno(int caderno) {
        this.caderno = caderno;
    }

    public int getCaneta() {
        return this.caneta;
    }

    public void setCaneta(int caneta) {
        this.caneta = caneta;
    }
}
