package trab1;

public class MaterialDidatico extends RecursosMateriais{
    public int livros = 0;
    public int quadroNegro = 0;
    public int quadroBranco = 0;
    public int giz = 0;
    public int apagador = 0;
    public int pincel = 0;

    public int getLivros() {
        return this.livros;
    }

    public void setLivros(int livros) {
        this.livros = livros;
    }

    public int getQuadroNegro() {
        return this.quadroNegro;
    }

    public void setQuadroNegro(int quadroNegro) {
        this.quadroNegro = quadroNegro;
    }

    public int getQuadroBranco() {
        return this.quadroBranco;
    }

    public void setQuadroBranco(int quadroBranco) {
        this.quadroBranco = quadroBranco;
    }

    public int getGiz() {
        return this.giz;
    }

    public void setGiz(int giz) {
        this.giz = giz;
    }

    public int getApagador() {
        return this.apagador;
    }

    public void setApagador(int apagador) {
        this.apagador = apagador;
    }

    public int getPincel() {
        return this.pincel;
    }

    public void setPincel(int pincel) {
        this.pincel = pincel;
    }
}
